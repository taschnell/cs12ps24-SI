# Example 1: Joining elements of a list into a string
my_list = ["apple", "banana", "cherry"]
joined_string = ", ".join(my_list)
print("Joined string:", joined_string)

# Example 2: Joining characters of a string into a single string
my_string = "Hello World"
joined_string = "-".join(my_string)
print("Joined string:", joined_string)

# Example 3: Joining elements of a tuple into a string
my_tuple = ("apple", "banana", "cherry")
joined_string = " and ".join(my_tuple)
print("Joined string:", joined_string)

# Example 4: Joining elements of a set into a string
my_set = {"apple", "banana", "cherry"}
joined_string = ", ".join(my_set)
print("Joined string:", joined_string)
