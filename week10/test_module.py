def add(x : int, y : int) -> int:
    """Add Two Numbers"""
    return x + y;

def subtract(x : int, y : int )-> int:
    """Subtract Two Numbers"""
    return x - y;

def multiply(x : int, y : int )-> float:
    """Multipy Two Numbers"""
    return x * y;

def divide(x : int, y : int) -> float:
    """Divide Two Numbers"""
    return x / y;
